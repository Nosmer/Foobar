#include <unistd.h>

void	ft_putstr(char *c)
{
	while (*c)
	{
		write(1, &(*c), 1);
		c++;
	}
}

int		main(int argc, char **argv)
{
	int		i;

	i = 0;
	if (argc >  1)
		ft_putstr(argv[1]);
	write(1, "\n", 1);
	return (0);
}
