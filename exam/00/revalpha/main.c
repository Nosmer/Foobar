#include <unistd.h>

void	ft_putchar(c)
{
	write(1, &c, 1);
}

int		main()
{
	int		i;

	i = 123;
	while (i-- != 97)
	{
		if (i % 2 == 0)
			ft_putchar(i);
		else
			ft_putchar(i - 32);
	}
	write(1, "\n", 1);
	return (0);
}
